package exemplos;
//Conversão de tipos
public class Exemplo04 {
   public static void main(String[] args) {
       int n1;
       double n2;

       //"forçar" conversão de double para int
       //casting
       n1 = (int)10.5;

       // a conversão do "menor" para o "maior é natural
       n2 = 10;

       System.out.println(n1);
       System.out.println(n2);

   } 
}